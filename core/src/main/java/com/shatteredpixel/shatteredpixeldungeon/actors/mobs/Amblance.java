public class Amblance extends Mob {

    {
        spriteClass = AmblanceSprite.class;

        alignment = Alignment.ALLY;
        state = WANDERING;
        intelligentAlly = true;
        properties.add(Property.INORGANIC);
        viewDistance = 99;
        HP = HT = 999;
        defenseSkill = 999;
        EXP = 0;
        baseSpeed = 1.5f;

        immunities.add( Blizzard.class );
        immunities.add( CorrosiveGas.class );
        immunities.add( Electricity.class );
        immunities.add( Fire.class );
        immunities.add( Freezing.class );
        immunities.add( Inferno.class );
        immunities.add( ParalyticGas.class );
        immunities.add( StenchGas.class );
        immunities.add( StormCloud.class );
        immunities.add( ToxicGas.class );
        immunities.addAll(AntiMagic.RESISTS);
        immunities.add(Sleep.class );
    }

    private boolean seenBefore = false;

    @Override
    protected boolean act() {

        if (Dungeon.level.heroFOV[pos]){
            Bestiary.setSeen(getClass());
        }

        if (!seenBefore) {
            WndDialogueWithPic.dialogue(
                    new CharSprite[]{new AmblanceSprite()},
                    new String[]{"앰뷸런스"},
                    new String[]{
                            Messages.get(this, "notice")
                    },
                    new byte[]{
                            WndDialogueWithPic.IDLE
                    }
            );
        }

        for (Char c : Actor.chars()){
            if (c instanceof Kawasiri){
                this.beckon(c.pos);
            }
        }

        seenBefore = true;
        return super.act();
    }

    @Override
    public int attackProc(Char enemy, int damage) {

        if (enemy instanceof Diego ){

            GLog.n(Messages.get(Diego.class, "m"));

            this.die(null);

        }

        new ExplosiveTrap().set(target).activate();

        return damage;
    }


    @Override
    public int damageRoll() {
        return Random.NormalIntRange( 150, 155 );
    }

    @Override
    public int attackSkill( Char target ) {
        return 9999;
    }

    @Override
    public int drRoll() {
        return Random.NormalIntRange(9995, 9999);
    }


}