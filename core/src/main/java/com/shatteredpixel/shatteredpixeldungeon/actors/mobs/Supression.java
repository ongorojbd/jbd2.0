public class Supression extends Mob {
	
	{
		spriteClass = SupressionSprite.class;

		HP = HT = 205;
		defenseSkill = 15;
		baseSpeed = 2f;

		EXP = 15;
		maxLvl = 30;

		loot = new PotionOfShielding();
		lootChance = 0.25f;
	}

	@Override
	public int damageRoll() {
		return Random.NormalIntRange( 35, 50 );
	}

	@Override
	public int attackSkill( Char target ) {
		return 40;
	}

	@Override
	public int drRoll() {
		return Random.NormalIntRange(0, 5);
	}

	@Override
	public int attackProc( Char hero, int damage ) {
		damage = super.attackProc( enemy, damage );
		if (this.buff(Doom.class) == null) {
			if (Random.Int(2) == 0) {
				Sample.INSTANCE.play(Assets.Sounds.OVERDRIVE);
				Buff.prolong(this, Haste.class, Haste.DURATION);
			}
		}
		return damage;
	}


	@Override
	public float lootChance() {
		return super.lootChance() * ((13f - Dungeon.LimitedDrops.NECRO_HP.count) / 13f);
	}

	@Override
	public Item createLoot(){
		Dungeon.LimitedDrops.NECRO_HP.count++;
		return super.createLoot();
	}


}
